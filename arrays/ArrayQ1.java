package arrays;

public class ArrayQ1 {
	public static void main(String[] args) {
		int[] arr = new int[3];
		System.out.println("Length of integer Array is: " + arr.length);
		char[] ch = new char[3];
		System.out.println("Length of character Array is: " + ch.length);
		double[] db = new double[3];
		System.out.println("Length of double Array is: " + db.length);
		byte[] bt = new byte[2];
		System.out.println("Length of byte Array is: " + bt.length);
		float[] fl = new float[3];
		System.out.println("Length of float Array is: " + fl.length);
		short[] sh = new short[3];
		System.out.println("Length of short Array is: " + sh.length);
		boolean[] bl = new boolean[3];
		System.out.println("Length of boolean Array is: " + bl.length);
		long[] lg = new long[90];
		System.out.println("Length of Long Array is: " + lg.length);
	}

}
